import { test } from 'node:test';
import assert from 'node:assert';

import { createSessionMaintenanceService } from '@/lib/services/sessions';

type LogRecord = {
  id: string;
  userId: string;
  machineId: string | null;
  decidedSiteId: string | null;
  decidedSiteNameSnapshot: string | null;
  workDescriptionSnapshot: string | null;
  workDate: string;
  stampedAtIso: string;
  stampType: 'IN' | 'OUT';
  autoGenerated: boolean;
};

function createRepo(initialLogs: Record<string, LogRecord>) {
  const openSessions: Array<{ id: string; userId: string; startAtIso: string }> = [];
  const inserted: string[] = [];
  const closed: Array<{ sessionId: string; outLogId: string; durationMin: number; autoGenerated: boolean }> = [];
  const forcedClosed: Array<{ workDate: string; forcedEndAtJstTime: string }> = [];

  return {
    state: { openSessions, inserted, closed, forcedClosed },
    repo: {
      async getLogById(logId: string) {
        return initialLogs[logId] ?? null;
      },
      async findLatestOpenSessionByUser(userId: string) {
        const latest = openSessions
          .filter((row) => row.userId === userId)
          .sort((a, b) => Date.parse(b.startAtIso) - Date.parse(a.startAtIso))[0];
        return latest ? { id: latest.id, startAtIso: latest.startAtIso } : null;
      },
      async insertOpenSession(log: LogRecord) {
        if (inserted.includes(log.id)) return;
        inserted.push(log.id);
        openSessions.push({ id: `sess-${log.id}`, userId: log.userId, startAtIso: log.stampedAtIso });
      },
      async closeOpenSession(params: {
        sessionId: string;
        outLogId: string;
        endAtIso: string;
        durationMin: number;
        autoGenerated: boolean;
      }) {
        const idx = openSessions.findIndex((row) => row.id === params.sessionId);
        if (idx === -1) return false;
        openSessions.splice(idx, 1);
        closed.push({
          sessionId: params.sessionId,
          outLogId: params.outLogId,
          durationMin: params.durationMin,
          autoGenerated: params.autoGenerated,
        });
        return true;
      },
      async forceCloseOpenSessionsByWorkDate(params: { workDate: string; forcedEndAtJstTime: string }) {
        forcedClosed.push(params);
        return 0;
      },
    },
  };
}

test('IN log creates open session', async () => {
  const logIn: LogRecord = {
    id: 'log-in-1',
    userId: '11111111-1111-4111-8111-111111111111',
    machineId: '22222222-2222-4222-8222-222222222222',
    decidedSiteId: null,
    decidedSiteNameSnapshot: 'A現場',
    workDescriptionSnapshot: '積み込み',
    workDate: '2026-02-20',
    stampedAtIso: '2026-02-20T00:00:00.000Z',
    stampType: 'IN',
    autoGenerated: false,
  };
  const { repo, state } = createRepo({ [logIn.id]: logIn });
  const service = createSessionMaintenanceService({ repo, logger: console });

  await service.handleSessionAfterLogInsert(logIn.id);

  assert.strictEqual(state.openSessions.length, 1);
  assert.strictEqual(state.inserted.length, 1);
});

