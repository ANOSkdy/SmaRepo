test('OUT log closes latest open session and computes duration', async () => {
  const userId = '11111111-1111-4111-8111-111111111111';
  const logIn: LogRecord = {
    id: 'log-in-latest',
    userId,
    machineId: '22222222-2222-4222-8222-222222222222',
    decidedSiteId: null,
    decidedSiteNameSnapshot: 'A現場',
    workDescriptionSnapshot: '積み込み',
    workDate: '2026-02-20',
    stampedAtIso: '2026-02-20T01:00:00.000Z',
    stampType: 'IN',
    autoGenerated: false,
  };
  const logOut: LogRecord = {
    ...logIn,
    id: 'log-out-1',
    stampedAtIso: '2026-02-20T02:15:00.000Z',
    stampType: 'OUT',
  };

  const { repo, state } = createRepo({
    [logIn.id]: logIn,
    [logOut.id]: logOut,
  });
  const service = createSessionMaintenanceService({ repo, logger: console });
  await service.handleSessionAfterLogInsert(logIn.id);
  await service.handleSessionAfterLogInsert(logOut.id);

  assert.strictEqual(state.closed.length, 1);
  assert.strictEqual(state.closed[0]?.sessionId, 'sess-log-in-latest');
  assert.strictEqual(state.closed[0]?.durationMin, 75);
});

test('OUT with no open session is safely ignored', async () => {
  const logOut: LogRecord = {
    id: 'log-out-2',
    userId: '11111111-1111-4111-8111-111111111111',
    machineId: '22222222-2222-4222-8222-222222222222',
    decidedSiteId: null,
    decidedSiteNameSnapshot: 'A現場',
    workDescriptionSnapshot: '積み込み',
    workDate: '2026-02-20',
    stampedAtIso: '2026-02-20T02:15:00.000Z',
    stampType: 'OUT',
    autoGenerated: false,
  };
  const { repo, state } = createRepo({ [logOut.id]: logOut });
  const service = createSessionMaintenanceService({
    repo,
    logger: {
      warn: () => {},
      error: () => {},
    },
  });

  await service.handleSessionAfterLogInsert(logOut.id);

  assert.strictEqual(state.closed.length, 0);
  assert.strictEqual(state.openSessions.length, 0);
});

test('cron auto-generated OUT also closes open session', async () => {
  const userId = '11111111-1111-4111-8111-111111111111';
  const logIn: LogRecord = {
    id: 'log-in-cron',
    userId,
    machineId: '22222222-2222-4222-8222-222222222222',
    decidedSiteId: null,
    decidedSiteNameSnapshot: 'A現場',
    workDescriptionSnapshot: '積み込み',
    workDate: '2026-02-20',
    stampedAtIso: '2026-02-20T01:00:00.000Z',
    stampType: 'IN',
    autoGenerated: false,
  };
  const logOut: LogRecord = {
    ...logIn,
    id: 'log-out-cron',
    stampType: 'OUT',
    stampedAtIso: '2026-02-20T03:00:00.000Z',
    autoGenerated: true,
  };

  const { repo, state } = createRepo({ [logIn.id]: logIn, [logOut.id]: logOut });
  const service = createSessionMaintenanceService({ repo, logger: console });

  await service.handleSessionAfterLogInsert(logIn.id);
  await service.handleSessionAfterLogInsert(logOut.id);

  assert.strictEqual(state.closed.length, 1);
  assert.strictEqual(state.closed[0]?.autoGenerated, true);
});

test('force close uses work_date and JST forced time', async () => {
  const { repo, state } = createRepo({});
  const service = createSessionMaintenanceService({ repo, logger: console });

  const closedCount = await service.forceCloseOpenSessionsByWorkDate('2026-02-20', '17:30');

  assert.strictEqual(closedCount, 0);
  assert.strictEqual(state.forcedClosed.length, 1);
  assert.deepStrictEqual(state.forcedClosed[0], {
    workDate: '2026-02-20',
    forcedEndAtJstTime: '17:30',
  });
});
